package Queen;
/**
 * @param x - положение ферзя по горизонтали (в числовом значении)
 * @param y - положение ферзя по вертикали
 */
public class QueenFigure {
	int x;
	int y;
	/**
	 * 
	 * @param x - положение ферзя по горизонтали (в числовом значении)
	 * @param y - положение ферзя по вертикали
	 */
	public QueenFigure(int x, int y) {
		this.x = x;
		this.y = y;
	}
}